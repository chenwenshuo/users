package com.cws.users.designPattern.factoryPattern;

import java.math.BigDecimal;

/**
 * @author wenshuo.chen@hand-china.com 2019/11/12 21:01
 */
public class A {
    int a;
    Long ll;
    String b;
    BigDecimal c;
    Long d;

    public Long getLl() {
        return ll;
    }

    public void setLl(Long ll) {
        this.ll = ll;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public BigDecimal getC() {
        return c;
    }

    public void setC(BigDecimal c) {
        this.c = c;
    }

    public Long getD() {
        return d;
    }

    public void setD(Long d) {
        this.d = d;
    }
}
